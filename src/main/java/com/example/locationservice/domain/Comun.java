package com.example.locationservice.domain;

public class Comun {

    private Long idC;

    private String nombre;

    private Regi regi;

    public Long getIdC() {
        return idC;
    }

    public void setIdC(Long idC) {
        this.idC = idC;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public Regi getRegi() {
        return regi;
    }

    public void setRegi(Regi regi) {
        this.regi = regi;
    }
}
